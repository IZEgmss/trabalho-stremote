package com.fatec.comercio.forms;
public class ClienteForm {

    private String nomecliente;
    private String cpfcliente;
    private String datanasc;
    private String numerocasa;
    private Integer codsexo;
    private Integer codrua;
    private Integer codcidade;
    private Integer codbairro;
    private Integer codcep;

    public String getNomecliente() { return nomecliente; }
    public void setNomecliente(String nomecliente) { this.nomecliente = nomecliente; }
    public String getCpfcliente() { return cpfcliente; }
    public void setCpfcliente(String cpfcliente) { this.cpfcliente = cpfcliente; }
    public String getDatanasc() { return datanasc; }
    public void setDatanasc(String datanasc) { this.datanasc = datanasc; }
    public String getNumerocasa() { return numerocasa; }
    public void setNumerocasa(String numerocasa) { this.numerocasa = numerocasa; }
    public Integer getCodsexo() { return codsexo; }
    public void setCodsexo(Integer codsexo) { this.codsexo = codsexo; }
    public Integer getCodrua() { return codrua; }
    public void setCodrua(Integer codrua) { this.codrua = codrua; }
    public Integer getCodcidade() { return codcidade; }
    public void setCodcidade(Integer codcidade) { this.codcidade = codcidade; }
    public Integer getCodbairro() { return codbairro; }
    public void setCodbairro(Integer codbairro) { this.codbairro = codbairro; }
    public Integer getCodcep() { return codcep; }
    public void setCodcep(Integer codcep) { this.codcep = codcep; }
}
